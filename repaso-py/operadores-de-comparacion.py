# Operador      #Comparacion
# ==             Es igual que 
# !=             Es distinto de
# <              Es menor que
# >              Es mayor que 
# <=             Es menor o igual que
# >=             Es mayor o igual que
print(3==2)         #False
print(4!=3)         #True
print(5<2)          #False
print(7>1)          #True